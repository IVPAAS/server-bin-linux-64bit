#!/bin/bash

SYSTEM_INI_FILE=/etc/kaltura.d/system.ini
if [ -r "$SYSTEM_INI_FILE" ];then
    . $SYSTEM_INI_FILE
else
    echo "I could not source $SYSTEM_INI_FILE. Exiting."
    exit 1
fi

KALTURA_BIN=$BASE_DIR/bin
KALTURA_BIN_DIRS=$KALTURA_BIN
KALTURA_BIN_FFMPEG=$KALTURA_BIN_DIRS/mencoder-dir/ffmpeg32bit
KALTURA_BIN_MENCODER=$KALTURA_BIN_DIRS/mencoder-dir
KALTURA_BIN_MENCODER_CODECS=$KALTURA_BIN_DIRS/mencoder-dir/codecs
MPLAYER_HOME=$KALTURA_BIN_MENCODER
export MPLAYER_HOME
LD_LIBRARY_PATH=$KALTURA_BIN_MENCODER:$KALTURA_BIN_FFMPEG/lib:$KALTURA_BIN_MENCODER_CODECS $KALTURA_BIN_MENCODER/mencoder "$@"
